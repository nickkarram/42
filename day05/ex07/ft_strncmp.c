int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int x;

	x = 0;
	while ((s1[x] || s2[x]) && (x < n))
	{
		if (s1[x] > s2[x])
		{
			return (1);
		}
		if (s1[x] < s2[x])
		{
			return (-1);
		}
		x++;
	}
	return (0);
}
