int		ft_atoi(char *str)
{
	int x;
	int y;
	int n;

	x = 0;
	y = 0;
	while (str[x] == ' ' || str[x] == '\t' || str[x] == '\f' || str[x] == '\n'
		|| str[x] == '\r' || str[x] == '\v')
		x++;
	if (str[x] == '-')
	{
		n = 1;
		x++;
	}
	while (str[x] > '9')
		return (0);
	while (str[x] >= '0' && str[x] <= '9')
	{
		y *= 10;
		y += str[x] - '0';
		x++;
	}
	if (n == 1)
		return (-y);
	return (y);
}
