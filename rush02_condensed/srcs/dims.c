#include "header.h"

int	x_dim(char *c)
{
	int i;

	i = 0;
	while (c[i] != '\n')
		i++;
	return (i);
}

int	y_dim(char *c)
{
	int i;
	int n;

	i = 0;
	n = 0;
	while (c[i])
	{
		if (c[i] == '\n')
			n++;
		i++;
	}
	return (n);
}
